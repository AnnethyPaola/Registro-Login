    public void modificar() {

        int fila = TablaDatos.getSelectedRow();
        if (fila >= 0) {
            txtid.setText(TablaDatos.getValueAt(fila, 0).toString());
            txtusuario.setText(TablaDatos.getValueAt(fila, 1).toString());
            txtnombre.setText(TablaDatos.getValueAt(fila, 2).toString());
            txtapellido.setText(TablaDatos.getValueAt(fila, 3).toString());
            txtcorreo.setText(TablaDatos.getValueAt(fila, 4).toString());
            txttelefono.setText(TablaDatos.getValueAt(fila, 5).toString());

        } else {

            JOptionPane.showMessageDialog(null, "Seleccione fila.");
        }
    }
