    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed

        String campo = txtusuario.getText();
        String where = "";

        if (!"".equals(campo)) {

            where = "WHERE usuario ='" + campo + "'";

        }

        try {

            DefaultTableModel modelo = new DefaultTableModel();
            TablaDatos.setModel(modelo);

            PreparedStatement ps = null;
            ResultSet rs = null;
            Conexion conn = new Conexion();
            Connection con = conn.getConexion();

            String sql = "SELECT id, usuario, nombre, apellido, telefono, correo, last_session  FROM usuarios " + where;
            System.out.println(sql);
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();

            ResultSetMetaData rsMd = rs.getMetaData();
            int cantidadColumna = rsMd.getColumnCount();

            modelo.addColumn("Id");
            modelo.addColumn("usuario");
            modelo.addColumn("nombre");
            modelo.addColumn("apellido");
            modelo.addColumn("correo");
            modelo.addColumn("telefono");
            modelo.addColumn("last_session");

            while (rs.next()) {

                Object[] filas = new Object[cantidadColumna];

                for (int i = 0; i < cantidadColumna; i++) {
                    filas[i] = rs.getObject(i + 1);
                }

                modelo.addRow(filas);

            }

        } catch (SQLException ex) {

            System.err.println(ex.toString());

        }
    }//GEN-LAST:event_btnbuscarActionPerformed

