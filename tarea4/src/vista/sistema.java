        sqlusuarios modSql = new sqlusuarios();
        usuarios mod = new usuarios();

        String pass = new String(txtpassword.getPassword());
        String passCon = new String(txtcpassword.getPassword());

        if (txtusuario.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "El campo usuario esta vacio, por favor ingrese datos");
        }

        if (pass.equals("")) {
            JOptionPane.showMessageDialog(null, "El campo contraseña esta vacio, por favor ingrese datos");
        }

        if (passCon.equals("")) {
            JOptionPane.showMessageDialog(null, "El campo confirmar contraseña esta vacio, por favor ingrese datos");
        }

